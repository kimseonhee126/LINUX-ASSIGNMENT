input_location()
{
	read -n 1 input
	if [ $input == '' ]; then
		read -n 1 input
		if [ $input == '[' ]; then
			read -n 1 input
			if [ $input == 'A' ]; then
				operator=-2
			elif [ $input == 'B' ]; then
				operator=2
			elif [ $input == 'C' ]; then
				operator=1
			elif [ $input == 'D' ]; then
				operator=-1
			fi
		fi
	elif [ $input == `` ]; then
		operator=0
	fi
}

function move()
{
	input_location
	oper="$operator"
	if [ "$oper" -eq -2 ]; then
		temp=`expr $now_location - 2`
		now_location=`expr $temp % 6`
	elif [ "$oper" -eq 2 ]; then
		temp=`expr $now_location + 2`
		now_location=`expr $temp % 6`
	elif [ "$oper" -eq 1 ]; then
		temp=`expr $now_location + 1`
		now_location=`expr $temp % 6`
	elif [ "$oper" -eq -1 ]; then
		temp=`expr $now_location - 1`
		now_location=`expr $temp % 6`
	fi
}

