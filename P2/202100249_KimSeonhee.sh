function join()
{
	file=`cat ./auth.txt`
	line=`echo $file`
	database=`echo $line | tr "," "\n"`

	count=0
	id_success=1
	pw_success=1

	for data in $database
	do
		standard=`expr $count % 2`
		if [ "$standard" -eq 0 ]; then
			if [ "$count" -eq 0 -o "$count" -eq 4 ]; then
				if [ "$data" == $1 ]; then
					id_success=0
				else
					id_success=1
				fi
			else
				id_success=1
			fi
		elif [ "$standard" -eq 1 ]; then
			if [ "$count" -eq 1 -o "$count" -eq 5 ]; then
				if [ "$data" == $2 ]; then
					pw_success=0
				else
					pw_success=1
				fi
			else
				pw_success=1
			fi
		fi
		if [ "$id_success" -eq 0 -a "$pw_success" -eq 0 ]; then
			result=0
			break
		else
			result=1
		fi
		count=`expr $count + 1`
	done
}

